// Operadores de Atribuicao

// =    Atribuicao

// +=   Adicao
let firstNumber = 10
firstNumber += 2
console.log(firstNumber)

// -=   Subtracao
let secondNumber = 10
secondNumber -= 2
console.log(secondNumber)

// /=   Divisao
let thirdNumber = 10
thirdNumber /= 2
console.log(thirdNumber)

// *=   Multiplicacao
let fourthNumber = 10
fourthNumber *= 2
console.log(fourthNumber)

// %=   Resto
let fifthNumber = 10
fifthNumber %= 3
console.log(fifthNumber)
